# -- Comparisons --

# Check if 5 is equal to 5
print(5 == 5)  # This is true

# Check if 5 is greater than 5
print(5 > 5)  # This is false

# Check if 10 is not equal to 10
print(10 != 10)  # This is false

# -- Comparison Operators: ==, !=, >, <, >=, <= --

# -- is --
# Python also has the `is` keyword, but it can be confusing, so it's best to avoid it for now.

# Create two lists of friends
friends = ["Rolf", "Bob"]
abroad = ["Rolf", "Bob"]

# Check if the lists have the same content
print(friends == abroad)  # This is true

# Check if the lists are the exact same objects in memory (usually not recommended)
print(friends is abroad)  # This is false



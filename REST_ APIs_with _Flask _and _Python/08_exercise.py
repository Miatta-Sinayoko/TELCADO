# Prompt the user for the current day of the week.
day_of_week = input("What day of the week is it today? ")

# Check the day of the week and provide a message accordingly.
if day_of_week == "Monday":
    print("Have a great start to your week!")
elif day_of_week == "Friday":
    print("It's ok to finish a bit early!")
else:
    print("Full speed ahead!")

# Prompt the user for the current day of the week, and convert the input to lowercase.
day_of_week = input("What day of the week is it today? ").lower()

# Check the day of the week and provide a message accordingly.
if day_of_week == "monday":
    print("Have a great start to your week!")
elif day_of_week == "friday":
    print("It's okay to finish a bit early!")

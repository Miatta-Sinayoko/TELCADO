# Create a set of movies watched.
movies_watched = {"The Matrix", "Power Book IV", "Renfield"}

# Prompt the user to enter a movie they've watched recently.
user_movie = input("Enter something you've watched recently: ")

# Check if the user's input is in the set of movies watched and print the result.
print(user_movie in movies_watched)

